package com.hfad.tailorme;

public class CustomerInfo {
    public String name, phoneNumber, style,back,bust,waist,hips,fullLengthOfGown;
    public String lengthOfSkirt,lengthOfBlouse,sleeve,roundSleeve;
    public CustomerInfo(String name, String phoneNumber,String style, String back
    ,String bust, String waist, String hips, String fullLengthOfGown, String lengthOfSkirt,
      String lengthOfBlouse,String sleeve,String roundSleeve){

        this.name = name;
        this.phoneNumber = phoneNumber;
        this.style = style;
        this.back = back;
        this.bust = bust;
        this.waist = waist;
        this.hips = hips;
        this.fullLengthOfGown = fullLengthOfGown;
        this.lengthOfSkirt = lengthOfSkirt;
        this.lengthOfBlouse = lengthOfBlouse;
        this.sleeve = sleeve;
        this.roundSleeve = roundSleeve;
    }


}
